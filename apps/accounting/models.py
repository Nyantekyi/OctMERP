class ChartsOfAccount(TenantAwareModel):
    """
    Defines the account types available in the ledger.
    acc_number is auto-generated by account_type series on first save.
    Normal balance is auto-derived from account_type.
    """
    ACCOUNT_TYPE_CHOICES = (
        ("Assets", _("Assets")),
        ("Expenses", _("Expenses")),
        ("Liabilities", _("Liabilities")),
        ("Revenues/Income", _("Revenues / Income")),
        ("Capital/Equity", _("Capital / Equity")),
    )
    BALANCE_TYPE_CHOICES = (
        ("Debit", _("Debit")),
        ("Credit", _("Credit")),
    )
    # Series ranges: Assets 10M, Liabilities 20M, Capital 30M, Revenue 40M, Expenses 50M
    SERIES_STARTS = {
        "Assets": 10100000,
        "Liabilities": 20100000,
        "Capital/Equity": 30100000,
        "Revenues/Income": 40100000,
        "Expenses": 50100000,
    }
    DEBIT_TYPES = {"Assets", "Expenses"}
    CREDIT_TYPES = {"Liabilities", "Revenues/Income", "Capital/Equity"}

    acc_number = models.PositiveIntegerField(_("Account Number"), editable=False)
    name = models.CharField(_("Account Name"), max_length=100, unique=True)
    description = models.TextField(_("Description"), blank=True, null=True)
    account_type = models.CharField(
        _("Account Type"), max_length=20, choices=ACCOUNT_TYPE_CHOICES
    )
    account_balance_type = models.CharField(
        _("Normal Balance"), max_length=10, choices=BALANCE_TYPE_CHOICES, editable=False
    )
    is_system_account = models.BooleanField(
        _("System Account?"), default=False,
        help_text=_("System accounts cannot be deleted.")
    )
    allow_direct_posting = models.BooleanField(_("Allow Direct Posting?"), default=True)

    class Meta:
        verbose_name = _("Charts of Account")
        verbose_name_plural = _("Charts of Accounts")
        ordering = ["acc_number"]

    def __str__(self):
        return f"{self.acc_number} — {self.name}"

    def get_absolute_url(self):
        return reverse("coa_detail", kwargs={"pk": self.pk})

    def save(self, *args, **kwargs):
        # Auto-derive normal balance
        if self.account_type in self.DEBIT_TYPES:
            self.account_balance_type = "Debit"
        elif self.account_type in self.CREDIT_TYPES:
            self.account_balance_type = "Credit"
        else:
            raise ValidationError(f"Invalid account type: {self.account_type}")

        # Auto-assign sequential acc_number on creation
        if self._state.adding:
            series_start = self.SERIES_STARTS.get(self.account_type, 10100000)
            step = 1000
            last = ChartsOfAccount.objects.filter(
                account_type=self.account_type
            ).aggregate(Max("acc_number"))["acc_number__max"]
            self.acc_number = (last + step) if last else series_start

        super().save(*args, **kwargs)


