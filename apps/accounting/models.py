class Transaction(TenantAwareModel):
    """
    Single debit or credit line against an Account.
    Enforces that debits == credits via TransactionDoc grouping.
    """
    TRANSACTION_TYPE_CHOICES = (
        ("Debit", _("Debit")),
        ("Credit", _("Credit")),
    )

    notes = models.ForeignKey(
        TransactionDoc, verbose_name=_("Transaction Document"),
        on_delete=models.CASCADE, related_name="transactions"
    )
    account = models.ForeignKey(
        Account, verbose_name=_("Account"),
        on_delete=models.CASCADE, related_name="account_transactions"
    )
    coa = models.ForeignKey(
        ChartsOfAccount, verbose_name=_("Chart of Account"),
        on_delete=models.CASCADE, related_name="coa_transactions", editable=False
    )
    transaction_type = models.CharField(
        _("Type"), max_length=10, choices=TRANSACTION_TYPE_CHOICES
    )
    amount = MoneyField(
        _("Amount"), max_digits=20, decimal_places=2,
        default=0, default_currency=DEFAULT_CURRENCY, currency_choices=CURRENCY_CHOICES
    )
    conversion_rate = models.DecimalField(_("Conversion Rate"), max_digits=10, decimal_places=6, default=1)
    amount_default = MoneyField(
        _("Amount (Default Currency)"), max_digits=20, decimal_places=2,
        default=0, default_currency=DEFAULT_CURRENCY, currency_choices=CURRENCY_CHOICES
    )
    description = models.CharField(_("Description"), max_length=255, blank=True)

    class Meta:
        verbose_name = _("Transaction")
        verbose_name_plural = _("Transactions")
        ordering = ["-notes__date"]

    def __str__(self):
        return f"{self.account.acc_number} | {self.transaction_type} | {self.amount}"

    def save(self, *args, **kwargs):
        if not self.account_id:
            raise ValidationError(_("Account is required."))
        self.coa = self.account.account_type
        self.amount_default = self.amount / self.conversion_rate
        super().save(*args, **kwargs)

    def get_absolute_url(self):
        return reverse("transaction_detail", kwargs={"pk": self.pk})


