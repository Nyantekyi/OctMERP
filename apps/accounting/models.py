class Account(TenantAwareModel):
    """
    Individual ledger account tied to a specific entity (Branch, Tax, Staff…)
    via a GenericForeignKey.  acc_number is derived from the parent COA.
    """
    account_type = models.ForeignKey(
        ChartsOfAccount, verbose_name=_("Chart of Account"),
        on_delete=models.DO_NOTHING, related_name="accounts"
    )
    acc_number = models.PositiveIntegerField(_("Account Number"), editable=False, unique=True)
    name = models.CharField(_("Account Name"), max_length=255, blank=True)
    content_type = models.ForeignKey(ContentType, on_delete=models.DO_NOTHING)
    object_id = models.UUIDField()
    account_object = GenericForeignKey("content_type", "object_id")
    notes = models.TextField(blank=True, null=True)

    class Meta:
        verbose_name = _("Account")
        verbose_name_plural = _("Accounts")
        unique_together = ("object_id", "account_type")
        ordering = ["acc_number"]

    def __str__(self):
        return f"{self.acc_number} — {self.name or self.account_type.name}"

    def get_absolute_url(self):
        return reverse("account_detail", kwargs={"pk": self.pk})

    def save(self, *args, **kwargs):
        if self._state.adding:
            coa = ChartsOfAccount.objects.get(pk=self.account_type.pk)
            count = Account.objects.filter(account_type=self.account_type).count()
            self.acc_number = count + 1 + coa.acc_number
        super().save(*args, **kwargs)

    @property
    def running_balance(self):
        """Compute balance from posted transactions."""
        debit = (
            Transaction.objects.filter(account=self, transaction_type="Debit")
            .aggregate(t=Sum("amount_default_currency"))
            .get("t") or Decimal("0")
        )
        credit = (
            Transaction.objects.filter(account=self, transaction_type="Credit")
            .aggregate(t=Sum("amount_default_currency"))
            .get("t") or Decimal("0")
        )
        if self.account_type.account_balance_type == "Debit":
            return debit - credit
        return credit - debit


