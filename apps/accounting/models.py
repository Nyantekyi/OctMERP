"""
apps/accounting/models.py

Double-entry accounting system for the ERP.

Hierarchy:
    ChartsOfAccount  ←  Account  ←  Transaction
                                 ←  JournalEntryLine
    TransactionDoc   ←  Transaction
    JournalEntry     ←  JournalEntryLine

Auto-account creation:
    post_save signals on Branch, StaffProfile, ClientProfile, SupplierProfile,
    Tax, Bank, BankAccount fire create_account_instance() which creates the
    correct Account rows and back-links them.

COA bootstrap:
    post_save on Company calls load_initial_account_data() to bulk-create the
    full default chart of accounts for each new tenant.
"""

