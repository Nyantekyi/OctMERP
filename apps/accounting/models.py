def _resolveid(instance):
    """Return the UUID primary key of instance, traversing Staff wrapping."""
    for attr in ("id", "pk"):
        val = getattr(instance, attr, None)
        if val:
            return val
    return None


def _resolve_company(instance):
    """Walk common relationship chains to find the Company for an instance."""
    chains = [
        ["company"],
        ["branch", "department", "company"],
        ["department", "company"],
        ["staff", "company"],
    ]
    for chain in chains:
        current = instance
        for attr in chain:
            current = getattr(current, attr, None)
            if current is None:
                break
        if current:
            return current
    return None


@receiver(post_save, sender=Tax)
@receiver(post_save, sender=Bank)
@receiver(post_save, sender=BankAccount)
def create_account_instance(sender, instance, created, **kwargs):
    """Auto-create linked Account entries for financial entities."""
    if not created:
        return
    id_val = _resolveid(instance)
    ct = ContentType.objects.get_for_model(sender)
    model_name = ct.model  # lowercase

    try:
        if model_name == "tax":
            payable_coa = ChartsOfAccount.objects.get(name="Taxes Payable")
            expense_coa = ChartsOfAccount.objects.get(name="Tax Expense")
            payable_acc, _ = Account.objects.get_or_create(
                content_type=ct, object_id=id_val, account_type=payable_coa
            )
            expense_acc, _ = Account.objects.get_or_create(
                content_type=ct, object_id=id_val, account_type=expense_coa
            )
            Tax.objects.filter(pk=id_val).update(
                tax_payable_account=payable_acc,
                tax_expense_account=expense_acc,
            )

        elif model_name == "bank":
            notes_coa = ChartsOfAccount.objects.get(name="Notes Payable")
            interest_coa = ChartsOfAccount.objects.get(name="Interest Payable")
            notes_acc, _ = Account.objects.get_or_create(
                content_type=ct, object_id=id_val, account_type=notes_coa
            )
            interest_acc, _ = Account.objects.get_or_create(
                content_type=ct, object_id=id_val, account_type=interest_coa
            )
            Bank.objects.filter(pk=id_val).update(
                notes_payable_account=notes_acc,
                notes_interest_account=interest_acc,
            )

        elif model_name == "bankaccount":
            current_asset_coa = ChartsOfAccount.objects.get(name="Current Asset")
            cash_acc, _ = Account.objects.get_or_create(
                content_type=ct, object_id=id_val, account_type=current_asset_coa
            )
            BankAccount.objects.filter(pk=id_val).update(cash_account=cash_acc)

    except Exception as exc:
        raise ValidationError(
            _(f"Could not create ledger account for {sender.__name__}: {exc}. "
              "Ensure the Chart of Accounts has been initialised.")
        )


# Branch and user profile account creation is handled via signals registered
# in the post_save handlers below (imported lazily to avoid circular imports).

@receiver(post_save)
def create_branch_and_profile_accounts(sender, instance, created, **kwargs):
    """Lazy handler — Branch + profiles trigger account creation."""
    if not created:
        return
    from django.apps import apps
    BranchModel = apps.get_model("party", "Branch")
    StaffProfileModel = apps.get_model("party", "StaffProfile")
    ClientProfileModel = apps.get_model("party", "ClientProfile")
    SupplierProfileModel = apps.get_model("party", "SupplierProfile")

    if not isinstance(instance, (BranchModel, StaffProfileModel, ClientProfileModel, SupplierProfileModel)):
        return

    ct = ContentType.objects.get_for_model(sender)
    id_val = _resolveid(instance)

    try:
        if isinstance(instance, BranchModel):
            _create_branch_accounts(ct, id_val, instance)
        elif isinstance(instance, StaffProfileModel):
            _create_staff_accounts(ct, id_val, instance)
        elif isinstance(instance, ClientProfileModel):
            _create_client_accounts(ct, id_val, instance)
        elif isinstance(instance, SupplierProfileModel):
            _create_supplier_accounts(ct, id_val, instance)
    except Exception as exc:
        raise ValidationError(
            _(f"Could not create ledger account for {sender.__name__}: {exc}")
        )


def _create_branch_accounts(ct, id_val, instance):
    account_names = {
        "Assets": ["Inventory", "Accounts Receivables", "Cash"],
        "Liabilities": ["Accounts Payable", "Wages Payable", "Tips Payable"],
        "Capital/Equity": ["Capital"],
        "Revenues/Income": ["Revenue/Income", "Operational Income"],
        "Expenses": ["Regular Expense", "Depreciation Expense", "Marketing Expenses",
                     "Freight Expense", "Cost of Goods Sold"],
    }
    accounts_list = []
    for acct_type, names in account_names.items():
        for name in names:
            coa = ChartsOfAccount.objects.get(name=name, account_type=acct_type)
            acc, _ = Account.objects.get_or_create(
                content_type=ct, object_id=id_val, account_type=coa
            )
            accounts_list.append(acc)
    from apps.party.models import Branch
    branch = Branch.objects.get(pk=id_val)
    branch.branch_accounts.set(accounts_list)


